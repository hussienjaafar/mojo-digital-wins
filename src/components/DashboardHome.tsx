import { useEffect, useState } from "react";
import { useNavigate } from "react-router-dom";
import { Card, CardContent, CardDescription, CardHeader, CardTitle } from "@/components/ui/card";
import { Button } from "@/components/ui/button";
import { supabase } from "@/integrations/supabase/fixed-client";
import { 
  TrendingUp, 
  Users, 
  DollarSign, 
  BarChart3, 
  Building2, 
  Activity,
  ArrowUpRight,
  ArrowDownRight,
  Plus
} from "lucide-react";

interface DashboardStats {
  totalClients: number;
  activeClients: number;
  totalRevenue: number;
  totalSpend: number;
  avgROI: number;
  totalDonations: number;
  revenueChange: number;
  clientChange: number;
}

export function DashboardHome() {
  const navigate = useNavigate();
  const [stats, setStats] = useState<DashboardStats>({
    totalClients: 0,
    activeClients: 0,
    totalRevenue: 0,
    totalSpend: 0,
    avgROI: 0,
    totalDonations: 0,
    revenueChange: 0,
    clientChange: 0,
  });
  const [isLoading, setIsLoading] = useState(true);

  useEffect(() => {
    fetchDashboardStats();
  }, []);

  const fetchDashboardStats = async () => {
    try {
      // Using any to bypass type issues with autogenerated types
      const orgsQuery = (supabase as any).from('client_organizations').select('id, is_active');
      const metricsQuery = (supabase as any).from('daily_aggregated_metrics').select('total_funds_raised, total_ad_spend, total_sms_cost, roi_percentage, total_donations');
      
      const [orgsResult, metricsResult] = await Promise.all([orgsQuery, metricsQuery]);
      
      const organizations = orgsResult.data || [];
      const metrics = metricsResult.data || [];

      // Calculate totals
      const totalRevenue = metrics.reduce((sum: number, m: any) => sum + (Number(m.total_funds_raised) || 0), 0);
      const totalAdSpend = metrics.reduce((sum: number, m: any) => sum + (Number(m.total_ad_spend) || 0), 0);
      const totalSmsSpend = metrics.reduce((sum: number, m: any) => sum + (Number(m.total_sms_cost) || 0), 0);
      const totalSpend = totalAdSpend + totalSmsSpend;
      const totalDonations = metrics.reduce((sum: number, m: any) => sum + (Number(m.total_donations) || 0), 0);
      const avgROI = totalSpend > 0 ? ((totalRevenue - totalSpend) / totalSpend) * 100 : 0;

      setStats({
        totalClients: organizations.length,
        activeClients: organizations.filter((o: any) => o.is_active).length,
        totalRevenue,
        totalSpend,
        avgROI,
        totalDonations,
        revenueChange: 12.5,
        clientChange: 8.2,
      });
    } catch (error) {
      console.error('Error fetching dashboard stats:', error);
    } finally {
      setIsLoading(false);
    }
  };

  const formatCurrency = (value: number) => {
    return new Intl.NumberFormat('en-US', {
      style: 'currency',
      currency: 'USD',
      minimumFractionDigits: 0,
      maximumFractionDigits: 0,
    }).format(value);
  };

  const formatNumber = (value: number) => {
    return new Intl.NumberFormat('en-US').format(value);
  };

  if (isLoading) {
    return (
      <div className="flex items-center justify-center py-12">
        <div className="text-lg text-muted-foreground">Loading dashboard...</div>
      </div>
    );
  }

  return (
    <div className="space-y-8">
      {/* Welcome Section */}
      <div className="flex items-center justify-between">
        <div>
          <h2 className="text-3xl font-bold tracking-tight">Dashboard Overview</h2>
          <p className="text-muted-foreground mt-2">
            Monitor your organization's performance and key metrics
          </p>
        </div>
        <Button onClick={() => navigate('/admin/client-view/a1b2c3d4-e5f6-4a5b-8c9d-0e1f2a3b4c5d')}>
          <Plus className="h-4 w-4 mr-2" />
          View Demo
        </Button>
      </div>

      {/* Key Metrics Grid */}
      <div className="grid gap-6 md:grid-cols-2 lg:grid-cols-4">
        <Card className="hover:shadow-lg transition-shadow">
          <CardHeader className="flex flex-row items-center justify-between space-y-0 pb-2">
            <CardTitle className="text-sm font-medium">Total Revenue</CardTitle>
            <DollarSign className="h-4 w-4 text-primary" />
          </CardHeader>
          <CardContent>
            <div className="text-2xl font-bold">{formatCurrency(stats.totalRevenue)}</div>
            <p className="text-xs text-muted-foreground flex items-center mt-1">
              {stats.revenueChange > 0 ? (
                <>
                  <ArrowUpRight className="h-3 w-3 text-green-500 mr-1" />
                  <span className="text-green-500">+{stats.revenueChange}%</span>
                </>
              ) : (
                <>
                  <ArrowDownRight className="h-3 w-3 text-red-500 mr-1" />
                  <span className="text-red-500">{stats.revenueChange}%</span>
                </>
              )}
              <span className="ml-1">from last month</span>
            </p>
          </CardContent>
        </Card>

        <Card className="hover:shadow-lg transition-shadow">
          <CardHeader className="flex flex-row items-center justify-between space-y-0 pb-2">
            <CardTitle className="text-sm font-medium">Active Clients</CardTitle>
            <Building2 className="h-4 w-4 text-primary" />
          </CardHeader>
          <CardContent>
            <div className="text-2xl font-bold">{stats.activeClients}</div>
            <p className="text-xs text-muted-foreground flex items-center mt-1">
              <span className="text-muted-foreground">of {stats.totalClients} total</span>
              {stats.clientChange > 0 && (
                <>
                  <ArrowUpRight className="h-3 w-3 text-green-500 ml-2 mr-1" />
                  <span className="text-green-500">+{stats.clientChange}%</span>
                </>
              )}
            </p>
          </CardContent>
        </Card>

        <Card className="hover:shadow-lg transition-shadow">
          <CardHeader className="flex flex-row items-center justify-between space-y-0 pb-2">
            <CardTitle className="text-sm font-medium">Average ROI</CardTitle>
            <TrendingUp className="h-4 w-4 text-primary" />
          </CardHeader>
          <CardContent>
            <div className="text-2xl font-bold">{stats.avgROI.toFixed(1)}%</div>
            <p className="text-xs text-muted-foreground mt-1">
              Return on investment
            </p>
          </CardContent>
        </Card>

        <Card className="hover:shadow-lg transition-shadow">
          <CardHeader className="flex flex-row items-center justify-between space-y-0 pb-2">
            <CardTitle className="text-sm font-medium">Total Donations</CardTitle>
            <Activity className="h-4 w-4 text-primary" />
          </CardHeader>
          <CardContent>
            <div className="text-2xl font-bold">{formatNumber(stats.totalDonations)}</div>
            <p className="text-xs text-muted-foreground mt-1">
              Across all campaigns
            </p>
          </CardContent>
        </Card>
      </div>

      {/* Demo Organization Card */}
      <Card className="bg-gradient-to-br from-primary/10 to-primary/5 border-primary/20">
        <CardHeader>
          <CardTitle className="text-2xl flex items-center gap-2">
            ðŸŽ¯ Demo Organization
          </CardTitle>
          <CardDescription>
            Explore sample data with the Progressive Victory Fund organization
          </CardDescription>
        </CardHeader>
        <CardContent className="space-y-4">
          <div className="grid grid-cols-1 md:grid-cols-3 gap-4">
            <div className="p-4 bg-background rounded-lg border">
              <div className="text-sm text-muted-foreground">Total Funds Raised</div>
              <div className="text-2xl font-bold text-primary">$245,750</div>
            </div>
            <div className="p-4 bg-background rounded-lg border">
              <div className="text-sm text-muted-foreground">Ad Spend</div>
              <div className="text-2xl font-bold text-primary">$18,500</div>
            </div>
            <div className="p-4 bg-background rounded-lg border">
              <div className="text-sm text-muted-foreground">ROI</div>
              <div className="text-2xl font-bold text-primary">1,228%</div>
            </div>
          </div>
          <Button 
            onClick={() => navigate('/admin/client-view/a1b2c3d4-e5f6-4a5b-8c9d-0e1f2a3b4c5d')}
            className="w-full"
          >
            View Full Dashboard â†’
          </Button>
        </CardContent>
      </Card>

      {/* Quick Actions */}
      <div className="grid gap-4 md:grid-cols-3">
        <Card className="hover:shadow-lg transition-shadow cursor-pointer" onClick={() => navigate('/admin')}>
          <CardHeader>
            <CardTitle className="text-lg flex items-center gap-2">
              <Users className="h-5 w-5 text-primary" />
              Manage Clients
            </CardTitle>
          </CardHeader>
          <CardContent>
            <p className="text-sm text-muted-foreground">
              View and manage client organizations and their settings
            </p>
          </CardContent>
        </Card>

        <Card className="hover:shadow-lg transition-shadow cursor-pointer">
          <CardHeader>
            <CardTitle className="text-lg flex items-center gap-2">
              <BarChart3 className="h-5 w-5 text-primary" />
              View Reports
            </CardTitle>
          </CardHeader>
          <CardContent>
            <p className="text-sm text-muted-foreground">
              Access detailed analytics and performance reports
            </p>
          </CardContent>
        </Card>

        <Card className="hover:shadow-lg transition-shadow cursor-pointer">
          <CardHeader>
            <CardTitle className="text-lg flex items-center gap-2">
              <Activity className="h-5 w-5 text-primary" />
              Monitor Campaigns
            </CardTitle>
          </CardHeader>
          <CardContent>
            <p className="text-sm text-muted-foreground">
              Track active campaigns and their real-time metrics
            </p>
          </CardContent>
        </Card>
      </div>

      {/* Financial Overview */}
      <div className="grid gap-6 md:grid-cols-2">
        <Card>
          <CardHeader>
            <CardTitle>Spending Overview</CardTitle>
            <CardDescription>Total advertising and SMS costs</CardDescription>
          </CardHeader>
          <CardContent>
            <div className="space-y-4">
              <div className="flex items-center justify-between">
                <span className="text-sm font-medium">Total Spend</span>
                <span className="text-2xl font-bold">{formatCurrency(stats.totalSpend)}</span>
              </div>
              <div className="text-xs text-muted-foreground">
                Combined advertising and SMS campaign costs across all clients
              </div>
            </div>
          </CardContent>
        </Card>

        <Card>
          <CardHeader>
            <CardTitle>Performance Metrics</CardTitle>
            <CardDescription>Key performance indicators</CardDescription>
          </CardHeader>
          <CardContent>
            <div className="space-y-4">
              <div className="flex items-center justify-between">
                <span className="text-sm font-medium">Profit</span>
                <span className="text-2xl font-bold text-green-600">
                  {formatCurrency(stats.totalRevenue - stats.totalSpend)}
                </span>
              </div>
              <div className="text-xs text-muted-foreground">
                Net revenue after all campaign costs
              </div>
            </div>
          </CardContent>
        </Card>
      </div>
    </div>
  );
}
